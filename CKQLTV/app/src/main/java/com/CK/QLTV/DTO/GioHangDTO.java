package com.CK.QLTV.DTO;

public class GioHangDTO {

    int Magiohang;
    String Tengiohang;
    boolean DuocChon;


    public int getMagiohang() {
        return Magiohang;
    }

    public void setMagiohang(int magiohang) {
        Magiohang = magiohang;
    }

    public String getTengiohang() {
        return Tengiohang;
    }

    public void setTengiohang(String tengiohang) {
        Tengiohang = tengiohang;
    }

    public boolean isDuocChon() {
        return DuocChon;
    }

    public void setDuocChon(boolean duocChon) {
        DuocChon = duocChon;
    }
}
